/*Realizar un programa de un numeros positivos que sume solo los numeros pares*/

#include<stdio.h>
int main(){
	int num=1,suma=0;
	while(num>0){
		printf("Ingrese el numero");
		scanf("%d",&num);
	}
		if(num>0)
			suma=suma+num;
			
	printf("La suma de los numeros pares: %d",suma);
}




